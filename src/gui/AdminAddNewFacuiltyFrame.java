    private void addNewFacuiltyBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addNewFacuiltyBtnActionPerformed
        // TODO add your handling code here:
        if(!validateInputs())
        {   
            
            LoginPojo user=new LoginPojo();
            user.setName(txtName.getText().trim());
            user.setMobileNo(txtMob.getText().trim());
            user.setPassword(PwdTxt.getText().trim());
            user.setType("Facuilty");
            
            if(!PwdTxt.getText().trim().equalsIgnoreCase(CnfPwdTxt.getText().trim()))
            {
                JOptionPane.showMessageDialog(null,"Password & Confirm Password aren't same","Error",JOptionPane.INFORMATION_MESSAGE);
                return;
            }
            
            try
            {
                if(UsersDao.addNewUser(user))
                {
                    JOptionPane.showMessageDialog(null,"Facuilty Added successfully","Sucess",JOptionPane.INFORMATION_MESSAGE);
                    txtName.setText("");
                    txtMob.setText("");
                    PwdTxt.setText("");
                    CnfPwdTxt.setText("");
                    return;
                }  
                else
                {
                    JOptionPane.showMessageDialog(null,"Another Facuilty May register with the given mobile no","Sucess",JOptionPane.INFORMATION_MESSAGE);
                    return;
                }
            }
            catch(SQLException ex)
            {
                ex.printStackTrace();
                JOptionPane.showMessageDialog(null,"Error while adding HR","Error",JOptionPane.INFORMATION_MESSAGE);
                return;
            }
            
        }   
        else
        {   
            JOptionPane.showMessageDialog(null,"Please fill all fields","Un-Sucess",JOptionPane.INFORMATION_MESSAGE);
            return;
        }   
    }//GEN-LAST:event_addNewFacuiltyBtnActionPerformed

