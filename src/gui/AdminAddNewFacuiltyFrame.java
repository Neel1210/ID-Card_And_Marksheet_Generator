    private boolean validateInputs()
    {
        if(txtName.getText().equals("") || txtMob.getText().equals("") || PwdTxt.getText().equals("") || CnfPwdTxt.getText().equals(""))
        {
            if(pwdInputs())
            {
                return true;
            }
        }
        return false;
    }
    
    public boolean pwdInputs()
    {
        if(PwdTxt.getText().trim().equalsIgnoreCase(CnfPwdTxt.getText().trim()))
             return true;
        return false;
    }

